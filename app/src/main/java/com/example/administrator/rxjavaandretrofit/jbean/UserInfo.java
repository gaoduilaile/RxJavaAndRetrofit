    public void setUser_sex(Integer user_sex) {
        this.user_sex = user_sex;
    }

    public Integer getUser_age() {
        return user_age;
    }

    public void setUser_age(Integer user_age) {
        this.user_age = user_age;
    }

    public Integer getUser_eye_condition() {
        return user_eye_condition;
    }

    public void setUser_eye_condition(Integer user_eye_condition) {
        this.user_eye_condition = user_eye_condition;
    }

    public String getUser_address_name() {
        return user_address_name;
    }

    public void setUser_address_name(String user_address_name) {
        this.user_address_name = user_address_name;
    }
